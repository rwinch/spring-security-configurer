package sample;

import static sample.MyConfigurer.*;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.config.annotation.web.configuration.EnableWebSecurity;
import org.springframework.security.config.annotation.web.configuration.WebSecurityConfigurerAdapter;

/**
 * @author Rob Winch
 *
 */
@EnableWebSecurity
public class SecurityConfig extends WebSecurityConfigurerAdapter {

	/* (non-Javadoc)
	 * @see org.springframework.security.config.annotation.web.configuration.WebSecurityConfigurerAdapter#configure(org.springframework.security.config.annotation.web.builders.HttpSecurity)
	 */
	@Override
	protected void configure(HttpSecurity http) throws Exception {
		http
			// apply a domain specific DSL (i.e. maybe framework integration
			// or a company specific set of configuration)
			.apply(myDsl())
				// we can have custom DSL properties as well
				.loginPage("/custom")
				.and()
			// we can perform additional customizations
			.csrf().disable();
	}
}